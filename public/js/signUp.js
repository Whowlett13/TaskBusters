const logout = async () => {
  const response = await fetch("/api/Users/logout", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
  });

  if (response.ok) {
    document.location.replace("/");
  } else {
    alert(response.statusText);
  }
};

document.querySelector("#signUp").addEventListener("click", logout);
